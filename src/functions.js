export { timeConversion, iconMap };

function timeConversion(num) {
  let time = "";
  let number = num.toString().split("").reverse().join("");
  for (let i = 0; i < number.length; i++)
    if (i % 2 == 0) time += number.substr(i, 2) + ":";
  let res = time
    .split("", time.length - 1)
    .reverse()
    .join("");
  if (!/:/.test(res)) {
    res += ":00";
  }
  return res.replace(/^\d{1}:/, "0$&").replace(/:\d{1}$/, "$&0");
}

